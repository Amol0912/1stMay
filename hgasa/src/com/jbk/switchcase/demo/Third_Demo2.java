public class Third_Demo2 {

	public static void main(String[] args) 
	{
		
		String s= "Amol";
		switch (s)
		{
		case "Amol" : System.out.println("he is amol");
		break;
		case "nitin" : System.out.println("he is amol");
		break;
		case "joshya" : System.out.println("he is amol");
		break;
		case "kapil" : System.out.println("he is amol");
		break;
		case "sachin t" : System.out.println("he is amol");
		break;
		default: System.out.println("nothing");
		
		}
	}
	
	
	

	}


